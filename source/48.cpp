void rotate(vector<vector<int>>& matrix) {
    
    reverse(matrix.begin(), matrix.end());
    int n = matrix.size();
    
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < i; j++) {
            swap(matrix[i][j], matrix[j][i]);
        }
    }
    
}
